package model;

public class Cabecera {
	public String numBoleta;
	private String fchBoleta;
	private int codCliente;
	private int codVendedor;
	private double totalBoleta;
	public String getNumBoleta() {
		return numBoleta;
	}
	public void setNumBoleta(String numBoleta) {
		this.numBoleta = numBoleta;
	}
	public String getFchBoleta() {
		return fchBoleta;
	}
	public void setFchBoleta(String fchBoleta) {
		this.fchBoleta = fchBoleta;
	}
	public int getCodCliente() {
		return codCliente;
	}
	public void setCodCliente(int codCliente) {
		this.codCliente = codCliente;
	}
	public int getCodVendedor() {
		return codVendedor;
	}
	public void setCodVendedor(int codVendedor) {
		this.codVendedor = codVendedor;
	}
	public double getTotalBoleta() {
		return totalBoleta;
	}
	public void setTotalBoleta(double totalBoleta) {
		this.totalBoleta = totalBoleta;
	}
	
	
	
}
